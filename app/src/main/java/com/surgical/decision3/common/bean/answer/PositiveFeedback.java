package com.surgical.decision3.common.bean.answer;

public class PositiveFeedback
{
	int id;
	String template_narration;
	int continual_freq;

	public int getId()
	{
		return id;
	}

	public void setId(int id)
	{
		this.id = id;
	}

	public String getTemplate_narration()
	{
		return template_narration;
	}

	public void setTemplate_narration(String template_narration)
	{
		this.template_narration = template_narration;
	}

	public int getContinual_freq()
	{
		return continual_freq;
	}

	public void setContinual_freq(int continual_freq)
	{
		this.continual_freq = continual_freq;
	}

}
