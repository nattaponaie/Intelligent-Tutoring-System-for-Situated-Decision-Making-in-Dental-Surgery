package com.surgical.decision3.common.bean.answer;

public class OCOutlineShape
{
	int id;
	String shape_name;
	String shape_desc;
	int target_tooth_no;

	public int getId()
	{
		return id;
	}

	public void setId(int id)
	{
		this.id = id;
	}

	public String getShape_name()
	{
		return shape_name;
	}

	public void setShape_name(String shape_name)
	{
		this.shape_name = shape_name;
	}

	public String getShape_desc()
	{
		return shape_desc;
	}

	public void setShape_desc(String shape_desc)
	{
		this.shape_desc = shape_desc;
	}

	public int getTarget_tooth_no()
	{
		return target_tooth_no;
	}

	public void setTarget_tooth_no(int target_tooth_no)
	{
		this.target_tooth_no = target_tooth_no;
	}

}
