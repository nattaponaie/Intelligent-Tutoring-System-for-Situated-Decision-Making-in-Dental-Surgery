package com.surgical.decision3.common.bean.tool;

public class Spreader
{
	int id;
	String number;
	Double tip_diameter;
	Double d16_diamter;
	Double AAE;

	public int getId()
	{
		return id;
	}

	public void setId(int id)
	{
		this.id = id;
	}

	public String getNumber()
	{
		return number;
	}

	public void setNumber(String number)
	{
		this.number = number;
	}

	public Double getTip_diameter()
	{
		return tip_diameter;
	}

	public void setTip_diameter(Double tip_diameter)
	{
		this.tip_diameter = tip_diameter;
	}

	public Double getD16_diameter()
	{
		return d16_diamter;
	}

	public void setD16_diameter(Double d16_diamter)
	{
		this.d16_diamter = d16_diamter;
	}

	public Double getAAE()
	{
		return AAE;
	}

	public void setAAE(Double aAE)
	{
		AAE = aAE;
	}
}
